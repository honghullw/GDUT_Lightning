#include "headfile.h"

// *************************** 例程说明 ***************************
// 
// 测试需要准备逐飞科技MM32SPIIN27PS核心板一块
// 
// 调试下载需要准备逐飞科技CMSIS-DAP或Jlink调试下载器一个
// 
// 本例程初始化了逐飞科技MM32SPIIN27PS核心板上的A4/D7两个LED对应的引脚
// 并初始化了一个引脚作为按键输入 该引脚可修改
// 
// 烧录本例程后 可见D7对应LED闪烁 按键输入引脚输入高电平时A4亮起
// 
// 打开新的工程或者工程移动了位置务必执行以下操作
// 第一步 关闭上面所有打开的文件
// 第二步 project  clean  等待下方进度条走完
// 
// *************************** 例程说明 ***************************

// *************************** 例程说明 ***************************
#define KEY				C6
// *************************** 例程说明 ***************************

// **************************** 变量定义 ****************************
uint16 loop_count = 0;
uint8 io_input_state = 0;
// **************************** 变量定义 ****************************

// **************************** 代码区域 ****************************
int main(void)
{
	board_init(true);																// 初始化 debug 输出串口

	gpio_init(KEY, GPI, GPIO_HIGH, GPI_PULL_UP);									// 初始化引脚为上拉输入 默认高电平
	gpio_init(A4, GPO, GPIO_HIGH, GPO_PUSH_PULL);									// 初始化引脚为推挽输出 默认高电平
	gpio_init(D7, GPO, GPIO_HIGH, GPO_PUSH_PULL);									// 初始化引脚为推挽输出 默认高电平

	while(1)
	{
		if(loop_count < 50)															// 500ms
			gpio_set(D7, 1);														// 输出高 灭灯
		else if(loop_count >= 50 && loop_count < 100)								// 500ms
			gpio_set(D7, 0);														// 输出低 亮灯
		else
			loop_count = 0;															// 清空计时

		io_input_state = gpio_get(KEY);												// 获取输入引脚电平
		if(io_input_state)															// 高电平
			gpio_set(A4, 0);														// 输出低 亮灯
		else																		// 高电低
			gpio_set(A4, 1);														// 输出高 灭灯

		loop_count++;																// 计时自增
		systick_delay_ms(10);														// 时间间隔 10ms
	}
}
